function mostrarOcultar(){
			var statusDisplay = document.getElementById("mostrar").style.display;
			var valueEnlace = document.getElementById("noticia").value;

			if (statusDisplay == "block") {
				document.getElementById("noticia").innerHTML = 'Leer más';
				document.getElementById("mostrar").style.display='none';	
			} else {
				document.getElementById("noticia").innerHTML = 'Cerrar';
				document.getElementById("mostrar").style.display='block';
			}
		}

